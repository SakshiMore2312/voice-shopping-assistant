export const getSeasonalRecommendation = () => {
  const month = new Date().getMonth(); // 0 = Jan, 11 = Dec
  let season = "all";
  if (month >= 2 && month <= 4) season = "Spring";
  else if (month >= 5 && month <= 7) season = "Summer";
  else if (month >= 8 && month <= 10) season = "Autumn";
  else season = "Winter";

  const seasonalItems = catalog.filter(item => item.season === season);
  if (seasonalItems.length === 0) {
    return catalog.filter(item => item.season === "Summer");
  }
  return seasonalItems;
};

export const getSubstitutesFor = (itemName) => {
  const item = catalog.find(
    c => c.name.toLowerCase().includes(itemName.toLowerCase()) || 
         itemName.toLowerCase().includes(c.name.toLowerCase())
  );
  if (item && !item.inStock && item.substitute) {
    return catalog.find(c => c.name === item.substitute || c.id === item.substitute);
  }
  return null;
};
